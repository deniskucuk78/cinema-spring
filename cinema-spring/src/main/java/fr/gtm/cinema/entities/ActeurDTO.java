package fr.gtm.cinema.entities;

import java.io.Serializable;

public class ActeurDTO implements Serializable{
	private long id;
	private String civilite;
	private String nom;
	private String prenom;
	
	public ActeurDTO(Acteur acteur) {
		id = acteur.getId();
		civilite = acteur.getCivilite();
		nom = acteur.getNom();
		prenom = acteur.getPrenom();
	}
	
	public ActeurDTO() {

	}
	
	


	
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getCivilite() {
		return civilite;
	}

	public void setCivilite(String civilite) {
		this.civilite = civilite;
	}

	public String getNom() {
		return nom;
	}

	public void setNom(String nom) {
		this.nom = nom;
	}

	public String getPrenom() {
		return prenom;
	}

	public void setPrenom(String prenom) {
		this.prenom = prenom;
	}
	
	public Acteur toActeur() {
		Acteur acteur = new Acteur();
		acteur.setId(id);
		acteur.setCivilite(civilite);
		acteur.setNom(nom);
		acteur.setPrenom(prenom);
		return acteur;
		
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((civilite == null) ? 0 : civilite.hashCode());
		result = prime * result + (int) (id ^ (id >>> 32));
		result = prime * result + ((nom == null) ? 0 : nom.hashCode());
		result = prime * result + ((prenom == null) ? 0 : prenom.hashCode());
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ActeurDTO other = (ActeurDTO) obj;
		if (civilite == null) {
			if (other.civilite != null)
				return false;
		} else if (!civilite.equals(other.civilite))
			return false;
		if (id != other.id)
			return false;
		if (nom == null) {
			if (other.nom != null)
				return false;
		} else if (!nom.equals(other.nom))
			return false;
		if (prenom == null) {
			if (other.prenom != null)
				return false;
		} else if (!prenom.equals(other.prenom))
			return false;
		return true;
	}
	
	

}
